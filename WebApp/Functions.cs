namespace WebApp
{
    public class Functions
    {
        public bool numberEven(int number)
        {

            if (number % 2 == 0) { return true; }
            else { return false; }

        }

        public int numberDouble(int number)
        {

            return number * 2;

        }

        public String stringReturn(String str)
        {

            return str;

        }

    }
}
